#include "printf.h"

void	ft_putnstr(char const *s, int i, int j)
{
	int k;

	k = 0;
	if (s == NULL)
		return ;
	while (k < j)
	{
		if (s[i] == '%' && s[i + 1] == '%')
		{
			i++;
			k++;
		}
		write(1, &s[i], 1);
		i++;
		k++;
	}
}
